    public void fillData(NguoiDung nguoiDung){
        lb_chucvu.setText(nguoiDung.getIdChucVu().toString());
        txt_ma.setText(nguoiDung.getMa());
        txt_hoten.setText(nguoiDung.getHoTen());
        txt_ngaysinh.setText(nguoiDung.getNgaySinh().toString());
        txt_email.setText(nguoiDung.getEmail());
        txt_diachi.setText(nguoiDung.getDiaChi());
        lb_img.setIcon(resizeImage(nguoiDung.getAnh()));
    }

    public ImageIcon resizeImage(String imgPath) {
        ImageIcon imageIcon = new ImageIcon(imgPath);
        Image img = imageIcon.getImage();
        Image newImg = img.getScaledInstance(lb_img.getWidth(), lb_img.getHeight(), Image.SCALE_SMOOTH);
        return new ImageIcon(newImg);
    }
